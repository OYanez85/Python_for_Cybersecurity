# Extract from a list
my_list = ["a", "b", "c", "d", "e", "f",]
print(my_list[1])

# Concatenate two lists
my_list = ["a", "b", "c",]
number_list = [1, 2,]
print(my_list + number_list)

# Change a specific element in a list
my_list = ["a", "b", "c", "d", "e", "f"]
my_list[1] = 7
print(my_list)

# Use the insert method
my_list = ["a", "b",]
my_list.insert(1,7)
print(my_list)

# Use the remove method
my_list = ["a", "b", "c", "d", "e", "f"]
my_list.remove("d")
print(my_list)
